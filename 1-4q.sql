SELECT D.DEPTNO, D.DNAME, E.EMPNO, E.ENAME, E.SAL
FROM EMP E, DEPT D
WHERE E.DEPTNO = D.DEPTNO 
  AND E.SAL > 2000;


-- 3
SELECT E.DEPTNO, ROUND(AVG(E.SAL), 0) AS AVG_SAL, MAX(E.SAL) AS MAX_SAL, MIN(E.SAL) AS MIN_SAL, COUNT(*) AS CNT
FROM EMP E
JOIN DEPT D ON E.DEPTNO = D.DEPTNO
GROUP BY E.DEPTNO
ORDER BY E.DEPTNO;


