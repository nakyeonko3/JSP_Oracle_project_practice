SELECT 
    D.DEPTNO, 
    D.DNAME, 
    E.EMPNO, 
    E.ENAME, 
    E.MGR, 
    E.SAL, 
    M.EMPNO AS MGR_EMPNO, 
    M.ENAME AS MGR_ENAME 
FROM 
    DEPT D 
    LEFT JOIN EMP E 
        ON D.DEPTNO = E.DEPTNO 
    LEFT JOIN EMP M 
        ON E.MGR = M.EMPNO 
ORDER BY 
    D.DEPTNO;


